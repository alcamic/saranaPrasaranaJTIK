    private void kembaliBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kembaliBarangActionPerformed
   // Retrieve data from input fields
     String selectedItem = (String) ComboBarang.getSelectedItem();

    if (selectedItem == null || selectedItem.isEmpty()) {
        System.out.println("Pilih barang yang ingin dikembalikan.");
        return;
    }

    // Pisahkan `nama_peminjam` dan `nama_barang`
    String[] parts = selectedItem.split(" - ");
    if (parts.length < 2) {
        System.out.println("Format data ComboBox tidak valid.");
        return;
    }

    String namaPeminjam = parts[0].trim();
    String namaBarang = parts[1].trim();

    try {
        Connection c = KoneksiDatabase.getKoneksi();

        // Step 1: Ambil jumlah pinjam dari tabel `pinjambarang`
        String fetchQuery = "SELECT jumlah_pinjam FROM pinjambarang WHERE nama_peminjam = ? AND nama_barang = ? AND tanggal_kembali IS NULL;";
        PreparedStatement fetchStmt = c.prepareStatement(fetchQuery);
        fetchStmt.setString(1, namaPeminjam);
        fetchStmt.setString(2, namaBarang);
        ResultSet rs = fetchStmt.executeQuery();

        int jumlahPinjam = 0;

        if (rs.next()) {
            jumlahPinjam = rs.getInt("jumlah_pinjam");
        } else {
            System.out.println("Data peminjaman tidak ditemukan.");
            return;
        }

        rs.close();
        fetchStmt.close();

        // Step 2: Perbarui tabel `databarang` untuk mengembalikan stok
        String updateStockQuery = "UPDATE databarang SET jumlah_total = jumlah_total + ?, jumlah_baik = jumlah_baik + ?, jumlah_kurang_baik = jumlah_kurang_baik WHERE nama_barang = ?;";
        PreparedStatement updateStockStmt = c.prepareStatement(updateStockQuery);
        updateStockStmt.setInt(1, jumlahPinjam);
        updateStockStmt.setInt(2, jumlahPinjam); // Diasumsikan semua barang kembali dalam kondisi baik
        updateStockStmt.setString(3, namaBarang);
        updateStockStmt.executeUpdate();
        updateStockStmt.close();

        // Step 3: Perbarui tanggal kembali di tabel `pinjambarang`
        String updateReturnDateQuery = "UPDATE pinjambarang SET tanggal_kembali = CURRENT_DATE WHERE nama_peminjam = ? AND nama_barang = ? AND tanggal_kembali IS NULL;";
        PreparedStatement updateReturnDateStmt = c.prepareStatement(updateReturnDateQuery);
        updateReturnDateStmt.setString(1, namaPeminjam);
        updateReturnDateStmt.setString(2, namaBarang);
        updateReturnDateStmt.executeUpdate();
        updateReturnDateStmt.close();

        System.out.println("Barang berhasil dikembalikan!");
    } catch (SQLException e) {
        e.printStackTrace();
        System.out.println("Terjadi kesalahan saat mengembalikan barang.");
    } finally {
        refreshComboBox(); // Refresh ComboBox untuk menampilkan data terbaru
    }
    }//GEN-LAST:event_kembaliBarangActionPerformed

