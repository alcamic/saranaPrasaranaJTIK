    public void loadBarang(){
        try {
        Connection c = KoneksiDatabase.getKoneksi();
        Statement s = c.createStatement();

        // Query untuk mendapatkan nama_peminjam dan nama_barang
        String sql = "SELECT nama_peminjam, nama_barang, jumlah_pinjam FROM pinjambarang WHERE tanggal_kembali IS NULL;";
        ResultSet r = s.executeQuery(sql);

        // Kosongkan ComboBox sebelum memuat data baru
        ComboBarang.removeAllItems();

        while (r.next()) {
            // Gabungkan nama_peminjam dan nama_barang
            String namaPeminjam = r.getString("nama_peminjam");
            String namaBarang = r.getString("nama_barang");
            int jumlahPinjam = r.getInt("jumlah_pinjam");
            String combinedItem = namaPeminjam + " - " + namaBarang + " - " + jumlahPinjam;

            // Tambahkan ke ComboBarang
            ComboBarang.addItem(combinedItem);
        }

        r.close();
        s.close();
    } catch (Exception e) {
        e.printStackTrace();
        System.out.println("Terjadi kesalahan saat memuat data ke ComboBarang.");
    }
    }
    
